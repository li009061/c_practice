// 1、（作用域）
// 请回答以下问题
// 1.C语言总共有多少种作用域？
// 四种
// | 作用域类型                      | 说明                      | 示例                                  |
// | -------------------------- | ----------------------- | ----------------------------------- |
// | **块作用域（Block Scope）**      | 在 `{}` 代码块内声明的标识符       | 函数体、if/for/while 块内的变量              |
// | **函数作用域（Function Scope）**  | 仅在函数内部有效，指标签（label）     | `goto` 目标标签                         |
// | **文件作用域（File Scope）**      | 在所有函数外部声明的标识符，从声明点到文件末尾 | 全局变量、`static` 函数                    |
// | **原型作用域（Prototype Scope）** | 函数原型参数列表中的标识符           | `int foo(int a, int b);` 中的 `a`、`b` |



// 2.如果同一个作用域中，出现重名标识符，会怎样？
//会冲突，系统不知道你要用哪个，编译器会直接报错。

// 3.如果在相互并存的两个作用域中，出现重名标识符，会怎样？
//不会冲突。相互并存的作用域互不干扰，每个函数内的局部变量只在自己的作用域内有效，
// 即使同名也不会产生冲突，因为编译器能通过作用域区分它们。互不干扰，各自独立

// 4.如果在相互嵌套的两个作用域中，出现重名标识符，会怎样？
//如果有多个不同的作用域相互嵌套，那么小范围的作用域会临时 “遮蔽” 大范围的作用域中的同名标识符，
// 被 “遮蔽” 的标识符不会消失，只是临时失去可见性